def replace_bitmap(bitmap: str) -> str:
    new_bitmap = ""
    ind, message = 0, input("Enter the message to display with the bitmap.\n> ")
    for character in bitmap:
        if ind == len(message):
            ind = 0
        if character == "*":
            new_bitmap += message[ind]
            ind += 1
        else:
            new_bitmap += character

    return new_bitmap


